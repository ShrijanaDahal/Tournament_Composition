public class Tournament {

    private  Team wholeTeam;
    private Player thePlayer;

    public Tournament(Team wholeTeam) {
        this.wholeTeam = wholeTeam;
        this.thePlayer = thePlayer;

    }

    public Team getWholeTeam() {
        return wholeTeam;
    }

    public Player getThePlayer() {
        return thePlayer;
    }
}
